public class Prob2{
  public static void main(String[] args)
  {
    int distance = 2614;
    System.out.println("Distance in meters is: " + (distance * 1000) + " meters");
    System.out.println("Distance in feet is: " + (distance * 1000 * 3.28084) + " feet");
    System.out.println("Distance in inches is: " + (distance * 1000 * 3 * 12) + " inches");
    System.out.println("Distance in cm is: " + (distance * 1000 * 100) + " cms");
  }
}